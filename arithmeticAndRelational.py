num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))
# Arithmetic operations:

print(f"Arithmetic Operations:")
print(f"{num1} + {num2} = {num1 + num2}")   #Add.
print(f"{num1} - {num2} = {num1 - num2}")   #Sub.
print(f"{num1} * {num2} = {num1 * num2}")   #Mul.
print(f"{num1} / {num2} = {num1 / num2}")   #Div.
print(f"{num1} % {num2} = {num1 % num2}")   #Rem.
# Relational operations: 

print("\nRelational Operations:")   #EXTRA LINE
print(f"{num1} == {num2}: {num1 == num2}")  #equality
print(f"{num1} != {num2}: {num1 != num2}")  #non-equality
print(f"{num1} > {num2}: {num1 > num2}")    #greater than
print(f"{num1} < {num2}: {num1 < num2}")    #lesser than
print(f"{num1} >= {num2}: {num1 >= num2}")  #greater than equal to
print(f"{num1} <= {num2}: {num1 <= num2}")  #lesser than equal to